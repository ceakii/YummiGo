import RecipeContainer from "../../components/RecipeContainer"
import ChickenPorridgeImage from "/images/ChickenPorridge.jpg";

export default function ChickenPorridge() {
  return (
    <RecipeContainer
      title="Chicken Porridge"
      imageSrc={ChickenPorridgeImage}
    >
      {`
        Prep Time: 45 minutes
        Cook Time: 90 minutes
        Total Time: 2 hours 15 minutes
        Servings: 6-8



        
        
        Ingredients

        • 1 yellow onion
        • 1 (3 1/2-pound) whole chicken
        • 1 tablespoon sea salt
        • 1 1/2 tablespoons rock or granulated sugar
        • 1 teaspoon chicken or mushroom bouillon (optional)
        • 1 1/3 cups (250g) uncooked jasmine rice
        • 2-inch knob fresh ginger, peeled and finely chopped, divided
        • 1 1/2 tablespoons good-quality fish sauce
        • 2 teaspoons freshly ground black pepper, divided


        
        
        Directions

        1. Bring 2 1/2 quarts (10 cups) of water to a boil in a kettle or pot.

        2. Peel and cut the stem and root ends off the onion to create a flat top and bottom. Set a small frying pan over high heat, place the onion on its flat side to char until blackened, then flip to char the other flat side.
       
        3. Place the charred onion and chicken in a large pot. Pour the boiling water over it and season with salt, rock sugar, and chicken or mushroom bouillon, if using. Set the pot over low heat and simmer covered for 1 hour.
        
        4. Fill a large bowl with very cold water and set it next to the stovetop. Use a spider to carefully scoop the cooked chicken out of the pot into the cold water bath. Allow the chicken to chill for about 15 minutes in the water bath. Then, drain it into a colander set in the sink. This will stop the chicken from cooking and makes sure it stays very tender. Do not discard the hot chicken stock in the pot. You will use it to cook the rice porridge. You can discard the onion.
        
        5. While the chicken chills, rinse the rice well under cold running water (at least 3 times) and drain well. Add it to the chicken stock along with the ginger—set aside 1 teaspoon of the ginger for the dipping sauce. 
        
        6. Cover the pot with a lid and bring it to the boil over medium heat. Skim off any fat and foam that floats to the top with a large spoon. Reduce the heat to low and simmer for 25 to 30 minutes until the rice has blossomed and thickened the broth, stirring occasionally. Season the porridge with fish sauce and 1 teaspoon black pepper. If the porridge is too thick for your liking, you can thin it out by adding a little water.
        
        7. While the rice cooks, use your hands to tear the chicken meat along the grain into 1/3-inch-wide strips, removing and discarding the bones. Add the pulled chicken on a large plate and toss with the remaining 1 teaspoon black pepper.
        
        8. To serve, ladle the warm porridge into soup bowls and top with the chicken.



      `}
    </RecipeContainer>
  );
}


